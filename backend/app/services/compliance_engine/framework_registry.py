def get_framework(key: str) -> FrameworkDefinition | None:
    return FRAMEWORK_REGISTRY.get(key)


def get_control(framework_key: str, control_id: str) -> ControlDefinition | None:
    fw = get_framework(framework_key)
    if not fw:
        return None
    for ctrl in fw.controls:
        if ctrl.control_id == control_id:
            return ctrl
    return None


def list_frameworks() -> list[FrameworkDefinition]:
    return list(FRAMEWORK_REGISTRY.values())
